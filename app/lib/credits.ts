/**
 * Pay in native SOL instead of the SPL token. Useful for demos / a simpler
 * payment rail (no burn — SOL just goes to the treasury). Set
 * NEXT_PUBLIC_PAY_IN_SOL=true to enable.
 */
export const PAY_IN_SOL = process.env.NEXT_PUBLIC_PAY_IN_SOL === 'true'

/** Wrapped-SOL mint — used to read the live SOL/USD price. */
export const WSOL_MINT = 'So11111111111111111111111111111111111111112'

/** Whether paid generation is active (either SOL mode or the SPL token). */
export function isPaymentLive(): boolean {
  // Native SOL needs a treasury (that's where it goes).
  if (PAY_IN_SOL) return Boolean(TOKEN.TREASURY)
  // SPL token: needs a mint. A treasury is only required when some of the
  // payment is NOT burned — at 100% burn nothing goes to a treasury.
  if (!TOKEN.MINT) return false
  return BURN_BPS >= 10000 || Boolean(TOKEN.TREASURY)
}

/**
 * Share of each generation payment that is BURNED (basis points; 10000 = 100%).
 * Default is 100% — every payment is destroyed. Tunable via env.
 */
export const BURN_BPS = Math.max(
  0,
  Math.min(10000, Number(process.env.NEXT_PUBLIC_BURN_BPS || '10000'))
)

/**
 * PRICING — every generation has a fixed price in USD. Balances are stored in
 * whole US cents (integer) so a prepaid balance never drifts with the token
 * price. You top up by sending $PIXAI (valued at the LIVE DexScreener price
 * at purchase time); each generation then costs its exact USD value.
 *
 * Tune these freely — they're retail prices (provider cost + margin).
 */
export const USD_COSTS = {
  /** Each image generated by /api/generate. (~$0.03 cost) */
  image: 0.05,
  /** Each outpaint step by /api/extend. */
  extend: 0.05,
  /** 3D model — representative flat; the 3D route overrides with estimate3DUsd. */
  model3d: 5,
  /** AI planning calls — scene brief, prop "art director" ideation. */
  brief: 0.01,
  /** AI vision QA passes — tile + sprite "art director" reviews. */
  review: 0.01,
} as const

export type CreditAction = keyof typeof USD_COSTS

/** Cents per dollar — the unit balances and costs are stored in. */
export const CENTS = 100
export const usdToCents = (usd: number) => Math.round(usd * CENTS)
export const centsToUsd = (cents: number) => cents / CENTS

/** Cost of an action in cents (integer). */
export function actionCostCents(action: CreditAction): number {
  return usdToCents(USD_COSTS[action])
}


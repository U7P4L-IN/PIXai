export const FAUCET_AMOUNT = usdToCents(5)

/**
 * 3D model pricing — charged in $PIXAI, scaled by prompt complexity.
 * A simple prompt lands near $2, a rich/rigged/multi-material one near $15.
 * (3D studio ships as a later update; this is the pricing it will use.)
 */
export const MODEL3D_USD = {
  MIN: Number(process.env.NEXT_PUBLIC_MODEL3D_USD_MIN || '2'),
  MAX: Number(process.env.NEXT_PUBLIC_MODEL3D_USD_MAX || '15'),
} as const

const COMPLEXITY_HINTS =
  /\b(rig|rigged|riggable|animat\w*|texture[ds]?|pbr|normal\s*map|detailed|intricate|ornate|multiple|several|scene|character|creature|armou?r|mechanical|robot|realistic|hi[- ]?poly|high[- ]?poly|sculpt\w*)\b/gi

/** Estimate the USD price of a 3D generation from its prompt ($2–$15). */
export function estimate3DUsd(prompt: string): number {
  const words = prompt.trim().split(/\s+/).filter(Boolean).length
  const hits = (prompt.match(COMPLEXITY_HINTS) || []).length
  const score = Math.min(1, (words / 45) * 0.6 + (hits / 6) * 0.4)
  const usd = MODEL3D_USD.MIN + score * (MODEL3D_USD.MAX - MODEL3D_USD.MIN)
  return Math.round(usd * 100) / 100
}

/** Convert a USD price to a token amount at a given live token price ($/token). */
export function usdToTokens(usd: number, tokenUsdPrice: number): number {
  if (!tokenUsdPrice || tokenUsdPrice <= 0) return 0
  return usd / tokenUsdPrice
}

// ─────────────────────────────────────────────────────────────────────────────
// MARKETPLACE — user-to-user 3D model sales, settled in native SOL.
//
// Independent of the generation payment rail above: a buyer pays the SELLER's
// wallet directly in SOL (peer-to-peer), with an optional platform fee skimmed
// to the treasury. This works even before the token launches. Prices are set by
// the seller directly in SOL (no live-price conversion needed).
// ─────────────────────────────────────────────────────────────────────────────

/** Lamports per whole SOL (1e9). Kept local so this stays import-free/client-safe. */
export const LAMPORTS_PER_SOL = 1_000_000_000

/** Listing currency — native SOL or the $PIXAI SPL token. */
export type MarketCurrency = 'sol' | 'token'

export const MARKET = {
  /**
   * Platform fee per sale, in basis points (10000 = 100%). The fee is sent to
   * MARKET.TREASURY; the rest goes straight to the seller. Default 0 (no fee) —
   * flip on later with NEXT_PUBLIC_MARKET_FEE_BPS, no code change.
   */
  FEE_BPS: Math.max(0, Math.min(10000, Number(process.env.NEXT_PUBLIC_MARKET_FEE_BPS || '0'))),
  /** Fee recipient — reuses the same treasury wallet as generation payments. */
  TREASURY: process.env.NEXT_PUBLIC_TOKEN_TREASURY || '',
  /** Minimum listing price in SOL (guards against dust / zero-price listings). */
  MIN_SOL: Number(process.env.NEXT_PUBLIC_MARKET_MIN_SOL || '0.001'),
  /** Maximum listing price in SOL (sanity cap on seller input). */
  MAX_SOL: Number(process.env.NEXT_PUBLIC_MARKET_MAX_SOL || '10000'),
} as const

export const solToLamports = (sol: number) => Math.round(sol * LAMPORTS_PER_SOL)
export const lamportsToSol = (lamports: number) => lamports / LAMPORTS_PER_SOL

/**
 * Surface the marketplace in the site nav. Off by default — the marketplace
 * ships DORMANT (reachable only at /marketplace) until we flip this on.
 * Set NEXT_PUBLIC_MARKET_NAV=true (e.g. on localhost) to reveal the nav tab.
 */
export const MARKET_NAV = process.env.NEXT_PUBLIC_MARKET_NAV === 'true'

/**
 * Split a total SOL price into the seller's share and the platform fee share,
 * both in lamports. Authoritative — server and client compute the same way.
 */
export function splitSale(priceSol: number, feeBps: number = MARKET.FEE_BPS): {
  total: number
  fee: number
  seller: number
} {
  const total = solToLamports(priceSol)
  const fee = Math.floor((total * Math.max(0, Math.min(10000, feeBps))) / 10000)
  return { total, fee, seller: total - fee }
}

/**
 * Split a price into seller + platform-fee shares in the SAME unit as the price
 * (whole SOL or whole tokens). Currency-agnostic — used for the $PIXAI rail and
 * for display. The client converts to lamports / base units when building the tx.
 */
export function feeShares(price: number, feeBps: number = MARKET.FEE_BPS): {
  fee: number
  seller: number
} {
  const fee = (price * Math.max(0, Math.min(10000, feeBps))) / 10000
  return { fee, seller: price - fee }
}

